package cartest6;

public class Price {

	
		private int pnum;
		private int price;
		private int peiod;
		private int sum;
	
		public Price(int pnum, int price, int peiod, int sum) {
			this.pnum = pnum;
			this.price = price;
			this.peiod = peiod;
			this.sum = sum;
		}

		public int getPnum() {
			return pnum;
		}

		public void setPnum(int pnum) {
			this.pnum = pnum;
		}

		public int getPrice() {
			return price;
		}

		public void setPrice(int price) {
			this.price = price;
		}

		public int getPeiod() {
			return peiod;
		}

		public void setPeiod(int peiod) {
			this.peiod = peiod;
		}

		public int getSum() {
			return sum;
		}

		public void setSum(int sum) {
			this.sum = sum;
		}
		
		
		
	

}
